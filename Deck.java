
public class Deck {
	public String shape;
	public String num;
	
	Deck() {
		this.shape = "helper";
		this.num = "helper";
	}
	
	//SET-GET0
	public String getShape() {
		return shape;
	}
	public void setShape(String Shape) {
		this.shape= Shape;
	}
	
	//SET-GET1
	public String getNum() {
		return num;
	}
	public void setNum(String Num) {
		this.num= Num;
	}
}
